
x = "Hello World"  # string
print(x)
x = 50  # integer
print(x)
x = 60.5  # float
print(x)
x = 3j  # complex
print(x)
x = ["Asad", "sazzad", "jim"]  # list
print(x)
x = ("mehedi", "khalid", "raj")  # tuple
print(x)
x = {"name": "afjal", "age": 24}  # dict
print(x)
x = {"geeks", "for"}  # set
print(x)
x = True  # bool
print(x)
x = b"blue"  # binary
print(x)
